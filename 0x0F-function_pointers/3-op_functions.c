#include "3-calc.h"
/**
 * op_add - Start of function of the sum
 * @a: First number
 * @b: Second number
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Start of function of the difference
 * @a: First number
 * @b: Second number
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Start of the function of the product
 * @a: First number
 * @b: Second number
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Start of the function of the division
 * @a: Firet number
 * @b: Second number
 * Return: Division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Start of function of the remainder of the division
 * @a: First numbre
 * @b: Second number
 * Return: Remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
